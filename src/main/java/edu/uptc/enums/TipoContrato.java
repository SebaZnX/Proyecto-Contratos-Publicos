package edu.uptc.enums;

/**
 * Enumeración que define los diferentes tipos de contratos que pueden ser gestionados en el sistema.
 * Cada tipo de contrato puede tener características y reglas específicas.
 */
public enum TipoContrato {
    /**
     * Contrato para la prestación de servicios profesionales o técnicos.
     */
    CONTRATOPRESTACIONSERVICIOS,
    /**
     * Contrato para la adquisición de bienes o productos.
     */
    CONTRATOCOMPRAVENTA,
    /**
     * Contrato para la ejecución de obras públicas.
     */
    CONTRATOOBRAPUBLICA
}
