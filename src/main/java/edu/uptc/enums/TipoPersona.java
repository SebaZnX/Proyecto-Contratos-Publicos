package edu.uptc.enums;

/**
 * Enumeración que define los tipos de persona que pueden interactuar con el sistema.
 * Esto es relevante para la clasificación de usuarios como contratantes o contratistas.
 */
public enum TipoPersona {
    /**
     * Representa una persona natural.
     */
    NATURAL,
    /**
     * Representa una persona jurídica (empresa u organización).
     */
    JURIDICA
}
